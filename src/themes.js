export function getCardGradients(themeId) {
    return themeCardGradients[themeId] || themeCardGradients.coffee_classic;
}

export const defaultTheme = 'coffee_classic';

export function applyTheme(themeId) {
    const theme = themes[themeId];
    if (!theme) return;

    const root = document.documentElement;
    Object.entries(theme.vars).forEach(([prop, value]) => {
        root.style.setProperty(prop, value);
    });

    // Handle dark theme body/text colors
    if (themeId === 'dark_roast') {
        root.style.setProperty('--white', '#1E1E36');
        root.classList.add('theme-dark');
    } else {
        root.style.setProperty('--white', '#FFFFFF');
        root.classList.remove('theme-dark');
    }

    localStorage.setItem('app_theme', themeId);
}

export function getStoredTheme() {
    return localStorage.getItem('app_theme') || defaultTheme;
}
